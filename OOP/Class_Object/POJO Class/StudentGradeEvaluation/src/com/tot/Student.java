package com.tot;

	public class Student {
	    private String name;
	    private int marks;
	    private String subject;

	    // Constructor
	    public Student(String name, int marks, String subject) {
	        this.name = name;
	        this.marks = marks;
	        this.subject = subject;
	    }

	    // Getters
	    public String getName() {
	        return name;
	    }

	    public int getMarks() {
	        return marks;
	    }

	    public String getSubject() {
	        return subject;
	    }

	}


