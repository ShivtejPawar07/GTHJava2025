package com.tot;

public class ServiceHistory {

    private int price;
    private String part;

    // Setter for price
    public void setPrice(int price) {
        this.price = price;
    }

    // Getter for price
    public int getPrice() {
        return price;
    }

    // Setter for part
    public void setPart(String part) {
        this.part = part;
    }


    public String getPart() {
        return part;
    }
}

